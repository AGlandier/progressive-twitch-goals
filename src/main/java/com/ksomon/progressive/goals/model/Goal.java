package com.ksomon.progressive.goals.model;

public class Goal {

    private String name;
    private int amount;

    public Goal() {
    }

    public Goal(String name, int amount) {
        this.name = name;
        this.amount = amount;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
}
